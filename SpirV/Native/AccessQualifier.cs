namespace SpirV.Native {
	public enum AccessQualifier {
		/// <summary>
		/// A read-only object.
		/// </summary>
		ReadOnly = 0,

		/// <summary>
		/// A write-only object.
		/// </summary>
		WriteOnly = 1,

		/// <summary>
		/// A readable and writable object.
		/// </summary>
		ReadWrite = 2
	}
}